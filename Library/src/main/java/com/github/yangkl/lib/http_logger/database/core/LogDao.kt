package com.github.yangkl.lib.http_logger.database.core

import androidx.room.Dao
import androidx.room.Delete
import androidx.room.Insert
import androidx.room.Query

@Dao
interface LogDao {

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //  数据库操作：增加
    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////
    //  数据库操作：增加
    ////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * 新增日志
     *
     * @param logEntities
     */
    @Insert
    fun insert(vararg logEntities: LogEntity?)

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //  数据库操作：删除
    ////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * 删除日志
     *
     * @param logEntities
     */
    @Delete
    fun deleteLogs(vararg logEntities: LogEntity?)


    ////////////////////////////////////////////////////////////////////////////////////////////////
    //  数据库操作：查询
    ////////////////////////////////////////////////////////////////////////////////////////////////

    /**
     * 按照创建时间查询第一个日志
     *
     * @return
     */
    @Query("SELECT * FROM tb_log ORDER BY create_time ASC LIMIT 1")
    fun queryFirstLog(): LogEntity?
}